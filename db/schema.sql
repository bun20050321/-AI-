    ON repair_contacts (product_model_id);

CREATE INDEX IF NOT EXISTS idx_repair_contacts_region
    ON repair_contacts (region_code);

CREATE INDEX IF NOT EXISTS idx_repair_contacts_status_priority
    ON repair_contacts (status, priority);

CREATE INDEX IF NOT EXISTS idx_golden_questions_model_version
    ON golden_questions (product_model_id, product_version_id);

CREATE INDEX IF NOT EXISTS idx_golden_questions_scenario
    ON golden_questions (scenario);

CREATE INDEX IF NOT EXISTS idx_golden_questions_status
    ON golden_questions (status);

CREATE INDEX IF NOT EXISTS idx_database_connection_checks_status
    ON database_connection_checks (check_status);

CREATE INDEX IF NOT EXISTS idx_database_connection_checks_checked_at
    ON database_connection_checks (checked_at);

COMMIT;
