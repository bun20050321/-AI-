    ON product_models (product_line);

CREATE INDEX IF NOT EXISTS idx_product_versions_model_id
    ON product_versions (product_model_id);

CREATE INDEX IF NOT EXISTS idx_product_versions_status
    ON product_versions (status);

CREATE INDEX IF NOT EXISTS idx_product_versions_manual_version
    ON product_versions (manual_version);

CREATE INDEX IF NOT EXISTS idx_manual_documents_model_version
    ON manual_documents (product_model_id, product_version_id);

CREATE INDEX IF NOT EXISTS idx_manual_documents_parse_status
    ON manual_documents (parse_status);

CREATE INDEX IF NOT EXISTS idx_manual_documents_source_file
    ON manual_documents (source_file);

CREATE INDEX IF NOT EXISTS idx_manual_sections_document_id
    ON manual_sections (document_id);

CREATE INDEX IF NOT EXISTS idx_manual_sections_parent_id
    ON manual_sections (parent_section_id);

CREATE INDEX IF NOT EXISTS idx_manual_sections_title
    ON manual_sections (section_title);

CREATE INDEX IF NOT EXISTS idx_manual_sections_page_range
    ON manual_sections (page_start, page_end);

CREATE INDEX IF NOT EXISTS idx_manual_chunks_model_version
    ON manual_chunks (product_model_id, product_version_id);

CREATE INDEX IF NOT EXISTS idx_manual_chunks_section_id
    ON manual_chunks (section_id);

CREATE INDEX IF NOT EXISTS idx_manual_chunks_section_title
    ON manual_chunks (section_title);

CREATE INDEX IF NOT EXISTS idx_manual_chunks_page_no
    ON manual_chunks (page_no);

CREATE INDEX IF NOT EXISTS idx_manual_chunks_content_hash
    ON manual_chunks (content_hash);

CREATE INDEX IF NOT EXISTS idx_manual_chunks_status
    ON manual_chunks (status);

CREATE INDEX IF NOT EXISTS idx_chunk_sources_chunk_id
    ON chunk_sources (chunk_id);

CREATE INDEX IF NOT EXISTS idx_chunk_sources_document_page
    ON chunk_sources (document_id, source_page_no);

